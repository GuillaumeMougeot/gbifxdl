# Image cleaner.
# It is a binary classifier to sort `valid` insect images from non-valid insect images.
# A valid insect image is image containing at least one adult insect clearly identifiable and if multiple insects appears, they must all be from the same species.
# A non-valid insect image can be one of:
# - Absent insect: a leaf, a tree, a museum tag, etc...
# - Noisy, blurry, incomplete insect: moving insect or behind a leaf, camera noise, image saturation etc...
# - Multiple insect species: more than one species of insect in the pictures
# - Non-adult stage: eggs, puppa, larvea etc...


